                                <h3>GEOPORTAL LORETO</h3>
                                <p>El Gobierno Regional de Loreto a través del Comité de Gestión de la Infraestructura
                                    de Datos Espaciales le da la bienvenida al Geoportal de la IDE Gore Loreto,
                                    plataforma tecnológica oficial, donde encontrará información espacial y estadística
                                    de datos geográficos que generan las diferentes unidades orgánicas del gobierno
                                    regional. Su acceso gratuito, amigable y dinámico le permite visualizar y descargar
                                    datos de calidad, ordenados, actualizados y estandarizados, que incluye servicios y
                                    aplicativos web para una mejor experiencia.<br /></p>
                                <p>Con la Ordenanza Regional N° 014-2014-GRL-CR se declara de interés regional y de
                                    prioridad la implementación de la Infraestructura de Datos Espaciales Regional. La
                                    IDE es el conjunto articulado de políticas, estándares, organizaciones, recursos
                                    humanos y tecnológicos destinados a facilitar la producción, uso y acceso a la
                                    información geográfica del Estado.<br /></p>
                                <p>El Geoportal de la IDE Gore Loreto es una herramienta para la gestión y desarrollo
                                    territorial del departamento Loreto, que se pone a disposición de las instituciones
                                    u organizaciones públicas o privadas y de la sociedad civil en cumplimiento de las
                                    políticas de gobierno de datos abiertos, electrónico y transparencia que forma parte
                                    de la Política Nacional de Modernización de la Gestión Pública, aprobada con Decreto
                                    Supremo N° 004-2013-PCM.<br /></p>
                            </div>
                            <div class="tab-pane fade" id="primary-pills-Antecedentes" role="tabpanel">
                                <h3>ANTECEDENTES</h3>
                                <p>
                                    El término infraestructura de datos espaciales fue acuñado por el Consejo Nacional
                                    de Investigación de los Estados Unidos en 1993, para definir un marco de trabajo que
                                    involucra tecnologías, políticas y acuerdos institucionales que faciliten la
                                    creación, intercambio y uso de datos e información espacial, relacionada a los
                                    recursos naturales. Con posibilidad de ser implementada que permita compartir
                                    información espacial dentro de una organización o más ampliamente para el uso a
                                    nivel regional, nacional o global. En todos los casos una infraestructura de datos
                                    espaciales provee un soporte institucional para la publicación, desarrollo,
                                    evaluación e intercambio de información espacial a través de la participación de
                                    productores y usuarios. Una Infraestructura de Datos Espaciales amplía un sistema de
                                    información geográfica (SIG) ya que asegura el uso de datos espaciales y estándares
                                    para crear grupos de datos autorizados (validados) y políticas que, a su vez, les
                                    brinden el soporte necesario.<br />
                                </p>
                                <p>
                                    En el Perú, la gestión de la información se establece claramente en el Decreto
                                    Supremo N° 133-2013-PCM, mediante el cual se establece el acceso e intercambio de
                                    información espacial entre entidades de la Administración Pública, en su artículo 4°
                                    establece que, para el cumplimiento de la presente normativa, las entidades de la
                                    Administración Pública deberán generar los servicios web que permitan la
                                    transferencia de datos espaciales o alojarlos en sus páginas web, enlaces (links) de
                                    descarga de las capas de información cuya administración es de su competencia y
                                    promover la implementación de sus Infraestructuras de Datos Espaciales como medio
                                    fundamental para compartir e intercambiar información espacial mediante servicios
                                    web interoperables.<br />
                                </p>
                                <p>Sin embargo, existen normas anteriores al Decreto Supremo N° 133-2013-PCM, que
                                    establecen algunos alcances sobre la implementación de la gestión de la información
                                    espacial:<br />
                                <ul>
                                    <li>En el artículo 32° de la Ley Orgánica de Gobiernos Regionales, señala que los
                                        Gobiernos Regionales promueven y apoyan las iniciativas de conectividad e
                                        intercambio de información y experiencias de gobierno valiosas para la gestión,
                                        entre los gobiernos regionales y entre éstos y el gobierno nacional y gobiernos
                                        locales, haciendo uso de las herramientas tecnológicas disponibles.</li>
                                    <li>La Política de Modernización del Estado, Decreto Supremo N° 004-2013-PCM, tiene
                                        como uno de sus objetivos promover el gobierno electrónico a través del uso
                                        intensivo de las tecnologías de información y comunicación (TIC) como soporte a
                                        los procesos de planificación, producción y gestión de las entidades públicas
                                        permitiendo a su vez consolidar propuestas de gobierno abierto.</li>
                                    <li>La Ley Marco de Modernización de la Gestión del Estado, Ley N° 27658,
                                        concordante con la Ley N° 29158, Ley Orgánica del Poder Ejecutivo, establece en
                                        su Artículo 4°, que el proceso de modernización de la gestión del Estado tiene
                                        como finalidad fundamental la obtención de mayores niveles de eficiencia del
                                        aparato estatal para lograr una mejor atención a la ciudadanía, priorizando y
                                        optimizando el uso de los recursos públicos, para lograr un Estado al servicio
                                        de la ciudadanía.</li>
                                    <li>La Ley del Procedimiento Administrativo General, Ley N° 27444, establece en su
                                        Artículo 76°, el criterio de colaboración entre entidades públicas, las que
                                        deben proporcionar directamente los datos e información que posean, sea cual
                                        fuere su naturaleza jurídica o posición institucional, a través de cualquier
                                        medio, sin más limitación que la establecida por la Constitución o la ley, para
                                        lo cual se propenderá a la interconexión de equipos de procesamiento electrónico
                                        de información, u otros medios similares.</li>
                                    <li>La Ley N° 27806, Ley de Transparencia y Acceso a la Información Pública, tiene
                                        como finalidad promover la transparencia de los actos del Estado y regular el
                                        derecho fundamental del acceso a la información consagrado en el numeral 5 del
                                        Artículo 2° de la Constitución Política del Perú.</li>
                                    <li>La Resolución Ministerial N° 381-2008-PCM, aprueba los lineamientos y mecanismos
                                        para implementar la interconexión de equipos de procesamiento electrónico de
                                        información entre las entidades del Estado, contenidos en el documento técnico
                                        “Estándares y Especificaciones de Interoperabilidad del Estado Peruano”</li>
                                </ul>
                                </p>
                                <p>
                                    Con la Resolución Ministerial N° 241-2014-PCM, que Aprueba Directiva Nº
                                    001-2014-PCM/ONGEI, “Directiva sobre Estándares de Servicios Web de Información
                                    Georreferenciada para el Intercambio de Datos entre Entidades de la Administración
                                    Pública”, establece que las entidades de la Administración Pública que administran
                                    bases de datos georreferenciadas (productoras o centralizadoras de información),
                                    deberán promover la implementación gradual y coordinada de servicios interoperables
                                    de información georreferenciada, para proporcionar a los usuarios el acceso a los
                                    datos a través del uso de servicios y publicación de mapas en web que forman parte
                                    de la red.
                                </p>
                                <p>En ese marco, el Gobierno Regional de Loreto, desde 2012 ha implementado su
                                    Infraestructura de Datos Espaciales, el cual es un proceso en curso que tiene el
                                    objetivo de estandarizar la generación, transformación, intercambio, difusión y uso
                                    de la información espacial en la planificación territorial y toma de decisiones
                                    informadas. La Infraestructura de Datos Espaciales es un proceso transversal que
                                    brinda “información espacial de calidad” a los demás procesos que desarrolla el
                                    Gobierno Regional de Loreto, en el marco de sus competencias.</p>
                                <p>La Infraestructura de Datos Espaciales es clave porque evita la duplicación en la
                                    generación de la información espacial entre órganos de una misma institución, brinda
                                    el acceso a la información espacial actualizada, mejora la transparencia y
                                    eficiencia del gasto público, modernizar la gestión del Estado y contribuye a la
                                    gobernanza. La información espacial que produce el Gobierno Regional de Loreto es de
                                    gestión forestal, de comunidades nativas y campesinas, de áreas de conservación
                                    regional, de centros poblados, de zonificación ecológica y económica, de pesquerías,
                                    de educación, de salud, entre otros.</p>
                                <p>El Gobierno Regional de Loreto, estableció su política de gestión de información
                                    espacial, con Ordenanza Regional N° 014-2014-GRL-CR, declarando de interés regional
                                    y de prioridad el proceso de ordenar, estandarizar y actualizar la información
                                    espacial o georreferenciada del departamento de Loreto, mediante la implementación
                                    de la Infraestructura de Datos Espaciales Regional, con el objetivo de modernizar la
                                    gestión pública regional y mejorar el acceso e intercambio de la información.</p>
                                <p>Ante de la promulgación de la Ordenanza Regional N° 014-2014-GRL-CR, el Gobierno
                                    Regional de Loreto, contaba con un Comité promotor y coordinador del desarrollo,
                                    intercambio y el uso de datos y servicios de información espacial, que buscaba
                                    satisfacer las necesidades de generación, gestión, difusión y consulta de la
                                    información espacial del departamento de Loreto. Este comité fue reconocido mediante
                                    Resolución Ejecutiva Regional Nº 678-2012-GRL-P, de fecha 24.07.2012, denominado
                                    “Comité de Gestión de la Infraestructura de Datos Espaciales del Gobierno Regional
                                    de Loreto”, pretendió lograr un nivel óptimo de interoperabilidad que permita la
                                    integración de los componentes de la Infraestructura de Datos Espaciales con otros
                                    nodos de información pertenecientes a un gran sistema de información global.</p>
                                <p>Posteriormente, con Resolución Ejecutiva Regional Nº 265-2016-GRL-P, de fecha
                                    16.05.2016, el Comité fue reestructurado y paso a ser un órgano consultivo del
                                    Gobierno Regional de Loreto, encargado de elaborar propuestas de políticas,
                                    estrategias generales, mecanismos de coordinación para el intercambio, uso de datos
                                    y servicios de información geoespacial como estrategia de gestión para mejorar la
                                    transparencia y el servicio al ciudadano; considerado el Comité como un espacio
                                    técnico que tiene como objetivo la implementación, operación y mantenimiento la
                                    Infraestructura de Datos Espaciales del Gobierno Regional de Loreto.</p>
                                <p>
                                    La Infraestructura de Datos Espaciales también está establecido en los documentos de
                                    gestión del departamento de Loreto como son el Plan de Desarrollo Regional
                                    Concertado: Loreto al 2021 y el Plan Estratégico Institucional del Gobierno Regional
                                    de Loreto 2016-2018.
                                <ul>
                                    <li>Plan de Desarrollo Regional Concertado Loreto al 2021, aprobado con Ordenanza
                                        Regional N° 014-2015-GRL-CR, establece la implementación de la Infraestructura
                                        de Datos Espaciales Regional como una acción estratégica del Objetivo
                                        Estratégico 5: Mejorar la Infraestructura para la Competitiva Regional.</li>
                                    <li>Plan Estratégico Institucional del Gobierno Regional de Loreto 2016-2018,
                                        aprobado con Resolución Ejecutiva Regional N° 102-2016-GRL-P, establece a la
                                        Infraestructura de Datos Espaciales como la acción estratégica institucional
                                        19.1 “Implementar la infraestructura de datos espaciales en la región”.</li>
                                </ul>
                                </p>
                                <p>En el marco a la operación y mantenimiento de la Infraestructura de Datos Espaciales,
                                    el Gobierno Regional de Loreto cuenta con algunos documentos que normalizan la
                                    estructura mínima de almacenamiento de producción, el cual garantiza el ordenamiento
                                    de la producción de la información espacial:
                                <ul>
                                    <li>Catálogo de Objetos Fundamentales del departamento de Loreto. Aprobado mediante
                                        Resolución Ejecutiva Regional N° 661-2014-GRL-P, contiene las definiciones de
                                        los temas, grupos y objetos geográficos, sus atributos, dominios de atributos,
                                        relaciones y operaciones.</li>
                                    <li>Catálogo de Objetos Geográficos y Catálogo de Símbolos de Gestión Forestal”.
                                        Aprobado mediante Resolución Ejecutiva Regional N° 397-2015-GRL-P, es un
                                        documento que está definido como una primera aproximación abstracta y
                                        simplificada de la realidad, y en una estructura organiza los tipos de objetos
                                        espaciales, sus definiciones y características.</li>
                                    <li>Catálogo de Objetos Geográficos y Catálogo de Símbolos de Gestión de Áreas de
                                        Conservación Regional”. Aprobado mediante Resolución Ejecutiva Regional N°
                                        423-2016-GRL-P.</li>
                                    <li>Catálogo de Objetos Geográficos y Catálogo de Símbolos de Pesca de Consumo y
                                        Ornamentales”. En proceso de aprobación.</li>
                                    <li>Catálogo de Objetos Geográficos y Catálogo de Comunidades Nativas y Campesinas”.
                                        En proceso de elaboración.</li>
                                </ul>
                                </p>
                                <p>El fortalecimiento de capacidades a los miembros del Comité de Gestión y a los
                                    usuarios vinculados con la Infraestructura de Datos Espaciales del Gobierno Regional
                                    de Loreto ha sido necesario e indispensable, la Secretaría Técnica del Comité de
                                    Gestión han dictado cursos a los órganos del Gobierno Regional de Loreto, enmarcadas
                                    en el fortalecimiento en gestión de metadatos y creación y edición de metadatos en
                                    la web, cursos básicos de SIG aplicado a Gestión Forestal, entre otros temas; además
                                    se ha contribuido a fortalecer las capacidades de los funcionarios ARA San Martin y
                                    Huánuco.</p>
                                <p>Los miembros del Comité de Gestión se han capacitado a nivel nacional e
                                    internacional:
                                <ul>
                                    <li>Marzo 2016. Pasantía Corta Diseño y Organización de la Infraestructura de Datos
                                        Espaciales” realizada en la ciudad de Washington DC – EEUU, organizado por el
                                        Servicio Forestal de los Estados Unidos.</li>
                                </ul>
                                </p>
                                <p>Los eventos de presentación y difusión de los avances la Infraestructura de Datos
                                    Espaciales del Gobierno Regional de Loreto ha sido fundamental, en estos años se ha
                                    realizado diversos eventos como:
                                <ul>
                                    <li>Iquitos Marzo 2015. Presentación del documento técnico: “Catálogo de Objetos
                                        Geográficos de Datos Fundamentales del Departamento de Loreto.</li>
                                    <li>Lima Julio 2015. Evento Público “Configurando la Gestión Territorial:
                                        Experiencias y Propuestas desde la Amazonía Peruana”, donde se presentó a los
                                        tomadores de decisión, sector privado, agencias de cooperación, organizaciones
                                        de la sociedad civil y público en general los avances y propuestas para la
                                        gestión territorial en la Amazonía mostrando el desarrollo de la Infraestructura
                                        de Datos Espaciales Regional (IDER), indicadores de gobernanza en los procesos
                                        de ordenamiento territorial y la propuesta del Sistema de Información
                                        Territorial (SIT).</li>
                                    <li>Lima Julio 2015. Desayuno de trabajo para la “Presentación del Catálogo de
                                        Objetos Geográficos y Catálogo Objetos de Símbolos de Gestión Forestal”, que
                                        tuvo como objetivo difundir los avances del proceso de estandarización y
                                        normalización de la información espacial que genera el Programa Regional de
                                        Manejo de Recursos Forestales y de Fauna Silvestre, en el marco de su
                                        competencias en la temática Forestal.</li>
                                    <li>Iquitos Setiembre 2015. Presentación del documento técnico: “Catálogo de Objetos
                                        Geográficos de Gestión Forestal”.</li>
                                    <li>Iquitos Octubre 2015. Evento Internacional para la Consolidación de la
                                        Infraestructura de Datos Espaciales: “Integrando el uso de la información
                                        espacial en la gestión del territorio: experiencias para la construcción de la
                                        infraestructura de datos espaciales amazónicas”, teniendo como un producto el
                                        compromiso asumido por los gobiernos regionales de Ucayali, San Martín,
