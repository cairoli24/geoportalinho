		function datableFuntions(name){
			const namedetailRows = [];
			name.on('click', 'tbody td.dtr-control', function(event) {
			let tr = event.target.closest('tr');
			let row = name.row(tr);
			let idx = namedetailRows.indexOf(tr.id);

			if (row.child.isShown()) {
				tr.classList.remove('details');
				row.child.hide();
				namedetailRows.splice(idx, 1);
			} else {
				tr.classList.add('details');
				row.child(format(row.data())).show();

				if (idx === -1) {
				namedetailRows.push(tr.id);
				}
			}
			});

			name.on('draw', () => {
			namedetailRows.forEach((id, i) => {
				let el = document.querySelector('#' + id + ' td.dt-control');

				if (el) {
				el.dispatchEvent(new Event('click', {
					bubbles: true
				}));
				}
			});
			});
		}
